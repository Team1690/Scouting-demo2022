import "package:flutter/material.dart";

class LightTeam {
  LightTeam(
    final int id,
    final int number,
    final String name,
    final int colorsIndex,
  ) : this._inner(
          id,
          number,
          name,
          _teamNumberToColor[number] ?? _colors[colorsIndex],
          colorsIndex,
        );
  LightTeam.fromJson(final dynamic e)
      : this(
          e["id"] as int,
          e["number"] as int,
          e["name"] as String,
          e["colors_index"] as int,
        );
  LightTeam._inner(
    this.id,
    this.number,
    this.name,
    this.color,
    this.colorsIndex,
  );

  @override
  int get hashCode => Object.hashAll(<Object?>[id, number, colorsIndex, color]);

  final int id;
  final int number;
  final String name;
  final Color color;
  final int colorsIndex;

  @override
  bool operator ==(final Object other) =>
      other is LightTeam &&
      other.name == name &&
      other.id == id &&
      other.number == number &&
      other.colorsIndex == colorsIndex;
}

